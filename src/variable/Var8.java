package variable;

public class Var8 {

    public static void main(String[] args) {
        // 정수
        byte b = 127;
        short s = 32767;
        int i = 2147483647;
        long l = 10L;

        // 실수
        double d;
        float f = 10.0f;

        // 정수는 int와 long, 실수는 double, boolean, 문자는 String을 사용
    }
}
